interface SidebarProps extends BoxProps {
  onClose: () => void
}

const SidebarContent = ({ onClose, ...rest }: SidebarProps) => {
    const navigate = useNavigate();
    const {contacts} = useContext(ContactContext);
    // const LinkItems: Array<LinkItemProps> = contacts.map(contact => ({
    //     id: contact.id,
    //     name: contact.name
    // }));
    const handleClick = (contactId:number) => () =>{
        onClose();
        navigate(`/contacts/${contactId}`);
    }//curring functions, two functions two getther, first one takes id and returns a func can be used later on for nav
    
    const handleNewContact = () =>{
        onClose();
        navigate("/contacts/new");
    }
    return (
    <Box
      bg={useColorModeValue('white', 'gray.900')}
      borderRight="1px"
      borderRightColor={useColorModeValue('gray.200', 'gray.700')}
      w={{ base: 'full', md: 60 }}
      pos="fixed"
      h="full"
      {...rest}>
      <Flex h="20" alignItems="center" mx="8" justifyContent="space-between">
        <Button onClick={handleNewContact}>New</Button>
        <CloseButton display={{ base: 'flex', md: 'none' }} onClick={onClose} />
      </Flex>
      {contacts.map((contact) => (
        <NavItem key={contact.name} icon={null as any} onClick={handleClick(contact.id)}>
          {contact.name}
        </NavItem>
      ))}
    </Box>
  )
}

interface NavItemProps extends FlexProps {
  icon: IconType
  children: ReactText
}
const NavItem = ({ icon, children, ...rest }: NavItemProps) => {
  return (
    <Box
      as="a"
      href="#"
      style={{ textDecoration: 'none' }}
      _focus={{ boxShadow: 'none' }}>
      <Flex
        align="center"
        p="4"
        mx="4"
        borderRadius="lg"
        role="group"
        cursor="pointer"
        _hover={{
          bg: 'cyan.400',
          color: 'white',
        }}
        {...rest}>
        {icon && (
          <Icon
            mr="4"
            fontSize="16"
            _groupHover={{
              color: 'white',
            }}
            as={icon}
          />
        )}
        {children}
      </Flex>
    </Box>
  )
}

